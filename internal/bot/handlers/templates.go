// TemplatesModule handles /templates.
type TemplatesModule struct {
	deps *Deps
	bot  *tb.Bot
}

func NewTemplatesModule(deps *Deps) *TemplatesModule {
	return &TemplatesModule{deps: deps, bot: deps.Bot}
}

func (m *TemplatesModule) Name() string { return "templates" }

func (m *TemplatesModule) Register() {
	handler := safeHandler("templates", func(c tb.Context) error {
		kb := &tb.ReplyMarkup{}
		var rows [][]tb.InlineButton
		for _, t := range templates {
			rows = append(rows, []tb.InlineButton{
				{Unique: "tpl", Text: t.Title, Data: t.ID},
			})
		}
		kb.InlineKeyboard = rows
		return c.Send("📚 Шаблони типових тем запитів. Оберіть тему:", kb)
	})

	m.bot.Handle("/templates", handler)
	m.bot.Handle("📚 Шаблони", handler)

	// Template callback
	tplBtn := tb.InlineButton{Unique: "tpl"}
	m.bot.Handle(&tplBtn, safeHandler("tpl_detail", func(c tb.Context) error {
		_ = c.Respond()
		id := c.Callback().Data
		for _, t := range templates {
			if t.ID == id {
				text := fmt.Sprintf("📋 *%s*\n\n*Тема:*\n`%s`\n\n*Опис:*\n```\n%s\n```\n\n_Натисніть на текст щоб скопіювати, потім /new._",
					t.Title, t.Subject, t.Body)
				_ = c.Edit(text, tb.ModeMarkdown)
				return nil
			}
		}
		_ = c.Edit("Шаблон не знайдено.")
		return nil
	}))
}
